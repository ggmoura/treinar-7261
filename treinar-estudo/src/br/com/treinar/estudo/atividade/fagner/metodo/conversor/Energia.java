package br.com.treinar.estudo.atividade.fagner.metodo.conversor;

public class Energia {
	
	private String joules;
	private String btu;
	private String calorias;
	public String getJoules() {
		return joules;
	}
	public void setJoules(String joules) {
		this.joules = joules;
	}
	public String getBtu() {
		return btu;
	}
	public void setBtu(String btu) {
		this.btu = btu;
	}
	public String getCalorias() {
		return calorias;
	}
	public void setCalorias(String calorias) {
		this.calorias = calorias;
	}
	

}
